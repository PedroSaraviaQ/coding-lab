fun main() {
    var name = "Pedro"

    //* You can access the characters of a string using square brackets
    println(name[0])

    //* "length" works as a property, not a method like in Java
    println(name.length)

    //* Using "==" makes a call to "equals" method
    println(name == "Pedro")
    //! To compare references, use "==="
}