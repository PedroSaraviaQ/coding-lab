fun main() {

    //! In Kotlin, there's no primitive types, everything is treated as an object

    //* To explicitly declare the type of a variable, use ":" after the variable name
    var score: Int = 10
    var food: String = "Pizza"

    //* Or you can just initialize the variable and omit the value
    var isHungry: Boolean

    //* Arrays are declared using "arrayOf" function
    var numbers = arrayOf(1, 2, 3)
    var vowels: Array<String> = arrayOf("a", "e", "i", "o", "u")
}