//* Mathematical
console.log(3 + 4); //* Addition "+"
console.log(1 - 2); //* Subtraction "-"
console.log(5 * 6); //* Multiplication "*"
console.log(20 / 5); //* Division "/"
console.log(16 % 7); //* Modulus "%"
console.log(10 ** 2); //* Exponentiation "**"

//* Logical
console.log(true && false); //* And "&&"
console.log(false || true); //* Or "||"
console.log(!true); //* Not "!"

//* Comparison
console.log(3 < 1);
console.log(11 >= 11);

console.log(1 == true); //* Equality "=="
console.log(3 != "3"); //* Inequality "!="
console.log(0 === "0"); //* Strict equality "==="
console.log(7 !== 6); //* Strict Inequality "!=="

